"""
Задача 47

Расчёт ипотечного платежа

Задание:

Напишите программу для расчёта ежемесячного платежа по ипотеке. Запросите у пользователя сумму кредита, годовую процентную ставку и срок кредита в годах. Используйте формулу аннуитетного платежа.

Ответ:"""

principal = float(input("Введите сумму кредита: "))
annual_rate = float(input("Введите годовую процентную ставку (в %): "))
years = int(input("Введите срок кредита в годах: "))

monthly_rate = annual_rate / 12 / 100
months = years * 12

payment = principal * (monthly_rate * (1 + monthly_rate) ** months) / ((1 + monthly_rate) ** months - 1)

print(f"Ежемесячный платеж: {payment:.2f}")
